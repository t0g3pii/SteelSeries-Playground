export const MEDIA_APP_ICONS: Record<
  MediaAppIconId,
  { label: string; rows: readonly string[] }
> = {
  spotify: { label: "Spotify", rows: SPOTIFY },
  chrome: { label: "Chrome", rows: CHROME },
  firefox: { label: "Firefox", rows: FIREFOX },
  opera: { label: "Opera", rows: OPERA },
  vlc: { label: "VLC", rows: VLC },
  jellyfin: { label: "Jellyfin", rows: JELLYFIN },
  edge: { label: "Edge", rows: EDGE },
  media: { label: "Medien", rows: GENERIC },
};

function isFirefoxApp(appName: string): boolean {
  const lower = appName.toLowerCase();
  return (
    lower.includes("firefox") ||
    lower.includes("mozilla") ||
    FIREFOX_AUMID_FRAGMENTS.some((fragment) => lower.includes(fragment))
  );
}

/** Zeitzeile auf dem OLED nur für diese Apps. */
const MEDIA_TIMELINE_APP_IDS = new Set<MediaAppIconId>(["spotify", "jellyfin"]);

export function supportsMediaTimelineDisplay(appName: string): boolean {
  return MEDIA_TIMELINE_APP_IDS.has(resolveMediaAppIconId(appName));
}

export function resolveMediaAppIconId(appName: string): MediaAppIconId {
  const lower = appName.toLowerCase();

  if (lower.includes("spotify")) return "spotify";
  if (lower.includes("chrome")) return "chrome";
  if (isFirefoxApp(appName)) return "firefox";
  if (lower.includes("opera")) return "opera";
  if (lower.includes("vlc")) return "vlc";
  if (lower.includes("jellyfin")) return "jellyfin";
  if (lower.includes("msedge") || lower.includes("edge")) return "edge";

  return "media";
}

/** Anzeigename für UI (statt roher AppUserModelId). */
export function resolveMediaAppLabel(appName: string): string {
  const id = resolveMediaAppIconId(appName);
  if (id !== "media") return MEDIA_APP_ICONS[id].label;
  return appName.trim() || MEDIA_APP_ICONS.media.label;
}

export function getMediaAppIconRows(appName: string): readonly string[] {
  const id = resolveMediaAppIconId(appName);
  return MEDIA_APP_ICONS[id].rows;
}
